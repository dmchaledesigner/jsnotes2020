// DATA TYPES
    
  var number = 10; // integer
    console.log(number, typeof number); // shows the result and using the 'typeof' it shows its a number
    

  var title = 'Hello world'; // string
    console.log(title, typeof title); // shows the result and using the 'typeof' it shows its a string
    

  var cars = ['Merc', 'BMW', 'Ferrari']; // Array
    console.log(cars, typeof cars); // shows the result and using the 'typeof' it shows its an object
    
  var car = { // object
              doors: 2,
              color: "red",
              trans: "auto",
              lights: 'xenon'
            }
  
  console.log(car, typeof car); // shows the result and using the 'typeof' it shows its a an object
  console.log(car.doors);


  var condition = true; // true of false
  console.log(condition, typeof condition);  // shows the result and using the 'typeof' it shows its a boolean value


